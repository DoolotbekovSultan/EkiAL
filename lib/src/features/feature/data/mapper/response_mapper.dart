/**
 * Маппер ответов (Response Mapper)
 * 
 * Этот файл содержит специализированные мапперы для преобразования
 * ответов от внешних источников данных (API, сервисов) во внутренние
 * модели приложения. Обрабатывает структурирование и нормализацию ответов.
 * 
 * ## Основные обязанности:
 * - Преобразование сырых ответов API в структурированные DTO модели
 * - Обработка и нормализация различных форматов ответов (JSON, XML, etc.)
 * - Извлечение данных из оберток API ответов (data, meta, pagination)
 * - Валидация структуры и целостности входящих ответов
 * - Обработка ошибок и исключительных ситуаций в ответах
 * - Трансформация данных ответа согласно бизнес-требованиям
 * 
 * ## Ключевые компоненты:
 * 
 * ### ResponseMapper
 * Базовый класс для мапперов ответов:
 * - Стандартизация обработки различных форматов ответов
 * - Извлечение полезных данных из API оберток
 * - Обработка мета-данных и пагинационной информации
 * - Валидация обязательных полей в ответах
 * 
 * ### Типы мапперов ответов:
 * - **ApiResponseMapper** - для стандартных API ответов
 * - **PaginatedResponseMapper** - для пагинированных ответов
 * - **ErrorResponseMapper** - для обработки ошибок API
 * - **StreamResponseMapper** - для потоковых ответов
 * 
 * ### Специализированные мапперы:
 * - **SuccessResponseMapper** - обработка успешных ответов
 * - **ListResponseMapper** - для ответов с коллекциями данных
 * - **NestedResponseMapper** - для сложных вложенных структур
 * - **LegacyResponseMapper** - для работы с устаревшими API форматами
 * 
 * ## Особенности:
 * - Единообразная обработка разных API endpoints
 * - Изоляция бизнес-логики от специфики внешних API
 * - Поддержка разных стратегий парсинга ответов
 * - Обработка optional полей и значений по умолчанию
 * - Валидация типов данных и форматов в ответах
 * - Гибкая обработка изменений в API контрактах
 * 
 * ## Best Practices:
 * - Стандартизируйте структуру обработки всех API ответов
 * - Валидируйте обязательные поля согласно API документации
 * - Используйте try-catch для обработки ошибок парсинга
 * - Документируйте ожидаемые форматы ответов для каждого endpoint
 * - Тестируйте обработку нестандартных и ошибочных ответов
 * - Разделяйте мапперы по типам ответов (success, error, paginated)
 * - Используйте default значения для optional полей в ответах
 * - Реализуйте graceful degradation при изменении API контрактов
 */
