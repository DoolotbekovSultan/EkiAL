/**
 * Маппер сущностей (Entity Mapper)
 * 
 * Этот файл содержит специализированные мапперы для преобразования
 * между сущностями доменного слоя и другими представлениями данных.
 * Обеспечивает изоляцию доменной модели от деталей реализации.
 * 
 * ## Основные обязанности:
 * - Преобразование Domain Entity в DTO модели и обратно
 * - Конвертация между разными представлениями сущностей
 * - Валидация данных при трансформации сущностей
 * - Обработка сложных отношений и вложенных объектов
 * - Нормализация и денормализация данных сущностей
 * - Поддержка версионности и миграции структур сущностей
 * 
 * ## Ключевые компоненты:
 * 
 * ### EntityMapper
 * Базовый класс для мапперов сущностей:
 * - Специализированные методы для работы с сущностями
 * - Обработка отношений и агрегатов
 * - Валидация бизнес-правил при преобразовании
 * - Поддержка наследования и полиморфизма сущностей
 * 
 * ### Типы мапперов сущностей:
 * - **EntityToDtoMapper** - преобразование Entity → DTO
 * - **DtoToEntityMapper** - преобразование DTO → Entity
 * - **EntityToModelMapper** - преобразование Entity → Model
 * - **CrossEntityMapper** - преобразование между разными Entity
 * 
 * ### Специализированные мапперы:
 * - **AggregateMapper** - для работы с агрегатами и корневыми сущностями
 * - **ValueObjectMapper** - для преобразования объектов-значений
 * - **CollectionMapper** - для работы с коллекциями сущностей
 * - **PolymorphicMapper** - для полиморфных иерархий сущностей
 * 
 * ## Особенности:
 * - Сохранение инвариантов доменной модели при преобразовании
 * - Обработка сложных графов объектов и отношений
 * - Поддержка разных стратегий маппинга (shallow/deep)
 * - Валидация целостности данных после преобразования
 * - Обработка optional полей и default значений
 * - Поддержка кастомных преобразований для специфичных полей
 * 
 * ## Best Practices:
 * - Сохраняйте иммутабельность сущностей при преобразовании
 * - Валидируйте обязательные поля и бизнес-правила
 * - Используйте композицию для сложных преобразований
 * - Документируйте правила преобразования для каждой сущности
 * - Тестируйте граничные случаи и ошибки данных
 * - Разделяйте ответственность между специализированными мапперами
 * - Используйте фабричные методы для создания сложных сущностей
 * - Следите за производительностью при работе с большими графами объектов
 */
