/**
 * Удаленный источник данных (Remote Data Source)
 * 
 * Этот файл содержит реализацию источника данных для работы с удаленными API
 * и внешними сервисами. Отвечает за сетевые запросы и взаимодействие
 * с backend-системами приложения.
 * 
 * ## Основные обязанности:
 * - Выполнение HTTP запросов к REST API endpoints
 * - Обработка аутентификации и авторизации запросов
 * - Управление сессиями и токенами доступа
 * - Обработка сетевых ошибок, таймаутов и retry логики
 * - Сериализация и десериализация данных для API
 * - Поддержка различных форматов данных (JSON, FormData, multipart)
 * - Кэширование сетевых запросов и оптимизация трафика
 * 
 * ## Ключевые компоненты:
 * 
 * ### RemoteDataSource класс
 * Основной класс для работы с удаленными данными:
 * - Централизованное управление сетевыми запросами
 * - Конфигурация базовых параметров HTTP клиента
 * - Обработка ошибок и исключительных ситуаций
 * - Управление заголовками и параметрами запросов
 * 
 * ### Поддерживаемые операции:
 * - Стандартные HTTP методы (GET, POST, PUT, PATCH, DELETE)
 * - Загрузка файлов и бинарных данных (upload/download)
 * - Потоковая передача данных (streaming)
 * - Пакетные запросы (batch requests)
 * - WebSocket соединения для real-time данных
 * - Server-Sent Events (SSE) для push-уведомлений
 * 
 * ## Используемые библиотеки:
 * - **dio** - основной HTTP клиент с поддержкой интерцепторов
 * - **http** - альтернативный HTTP клиент (для простых запросов)
 * - **dio_retry** - для автоматических повторных попыток запросов
 * - **web_socket_channel** - для WebSocket соединений
 * - **connectivity_plus** - для проверки сетевого соединения
 * 
 * ## Особенности:
 * - Централизованная обработка ошибок и исключений
 * - Автоматическое обновление access/refresh токенов
 * - Поддержка разных environments (dev, staging, production)
 * - Интерцепторы для логирования и мониторинга запросов
 * - Поддержка cancelation tokens для отмены операций
 * - Конфигурируемые таймауты и retry стратегии
 * - Валидация SSL сертификатов и безопасность
 * 
 * ## Best Practices:
 * - Используйте base URL из конфигурации окружения
 * - Реализуйте единообразную обработку HTTP статусов
 * - Используйте интерцепторы для cross-cutting concerns
 * - Логируйте запросы и ответы для отладки
 * - Реализуйте timeout для всех сетевых операций
 * - Тестируйте сценарии плохого сетевого соединения
 * - Используйте cancellation tokens для отмены долгих запросов
 * - Валидируйте данные перед отправкой на сервер
 * - Документируйте API endpoints и их параметры
 */
