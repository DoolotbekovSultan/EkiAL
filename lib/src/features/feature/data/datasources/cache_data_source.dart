/**
 * Локальный источник данных фичи (Feature Local Data Source)
 * 
 * Этот файл содержит реализацию локального источника данных для конкретной фичи,
 * отвечающий за работу с локальным хранилищем данных, специфичных для данной фичи.
 * 
 * ## Основные обязанности:
 * - Сохранение и загрузка данных фичи в локальное хранилище
 * - Управление кэшем данных, специфичных для фичи
 * - Миграция данных фичи между версиями приложения
 * - Обеспечение целостности данных фичи в offline режиме
 * - Синхронизация локальных данных с удаленным источником
 * 
 * ## Отличия от shared/local_data_source:
 * - Содержит только данные, специфичные для данной фичи
 * - Реализует бизнес-логику работы с данными фичи
 * - Может иметь зависимости от доменных моделей фичи
 * - Оптимизирован для конкретных use cases фичи
 * 
 * ## Ключевые компоненты:
 * 
 * ### FeatureLocalDataSource
 * Основной класс для работы с локальными данными фичи:
 * - Специфичные методы для работы с данными фичи
 * - Оптимизированные запросы для use cases фичи
 * - Бизнес-логика валидации данных фичи
 * - Миграции схемы данных фичи
 * 
 * ### Типы хранимых данных:
 * - **Кэш данных фичи** - временные данные для производительности
 * - **Пользовательские предпочтения фичи** - настройки и конфигурации
 * - **Состояние фичи** - текущий прогресс и сессия
 * - **Оффлайн данные** - данные для работы без интернета
 * 
 * ## Используемые библиотеки:
 * - **hive** - для сложных объектов и отношений
 * - **shared_preferences** - для простых настроек
 * - **sqflite** - для реляционных данных (опционально)
 * - **path_provider** - для работы с путями файловой системы
 * 
 * ## Особенности:
 * - Специфичная оптимизация для use cases фичи
 * - Интеграция с доменными моделями фичи
 * - Поддержка сложных запросов и фильтрации
 * - Автоматическая миграция данных фичи
 * - Обработка конфликтов синхронизации
 * 
 * ## Best Practices:
 * - Инкапсулируйте логику работы с данными фичи в этом классе
 * - Используйте префиксы для ключей чтобы избежать конфликтов
 * - Реализуйте миграции для обратной совместимости
 * - Валидируйте данные перед сохранением
 * - Тестируйте сценарии отсутствия данных
 * - Документируйте схему хранения данных фичи
 * - Реализуйте очистку устаревших данных
 * - Используйте транзакции для критических операций
 */
