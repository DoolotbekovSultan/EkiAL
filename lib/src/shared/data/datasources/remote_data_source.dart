/**
 * Удаленный источник данных (Remote Data Source)
 * 
 * Этот класс предоставляет единый интерфейс для работы с удаленными API,
 * используемый несколькими фичами приложения. Отвечает за сетевые запросы
 * и взаимодействие с внешними сервисами.
 * 
 * ## Основные обязанности:
 * - Выполнение HTTP запросов к API endpoints
 * - Обработка аутентификации и авторизации
 * - Управление сессиями и токенами
 * - Обработка сетевых ошибок и таймаутов
 * - Кэширование часто запрашиваемых данных
 * - Поддержка offline-режима с последующей синхронизацией
 * 
 * ## Поддерживаемые операции:
 * - GET, POST, PUT, PATCH, DELETE запросы
 * - Загрузка файлов и медиа контента
 * - Потоковая передача данных
 * - Пагинация и бесконечная прокрутка
 * - Пакетные запросы (batch requests)
 * 
 * ## Используемые библиотеки:
 * - **dio** - для HTTP запросов и интерцепторов
 * - **dio_retry** - для повторных попыток запросов
 * - **connectivity_plus** - для проверки соединения
 * - **dio_cache_interceptor** - для кэширования запросов
 * 
 * ## Типы запросов:
 * - **Публичные запросы** - не требующие аутентификации
 * - **Защищенные запросы** - с JWT токеном авторизации
 * - **Запросы с файлами** - multipart/form-data
 * - **WebSocket соединения** - для real-time данных
 * 
 * ## Особенности:
 * - Автоматическая обработка токенов авторизации
 * - Retry логика для неудачных запросов
 * - Интерцепторы для логирования и мониторинга
 * - Поддержка разных форматов данных (JSON, FormData)
 * - Валидация ответов сервера
 * - Graceful degradation при потере соединения
 * 
 * ## Best Practices:
 * - Используйте base URL из конфигурации
 * - Реализуйте обработку различных HTTP статусов
 * - Используйте интерцепторы для общей логики
 * - Логируйте запросы для отладки
 * - Реализуйте timeout для всех запросов
 * - Тестируйте сценарии плохого соединения
 * - Используйте cancellation tokens для отмены запросов
 */

