/**
 * Источник кэшированных данных (Cache Data Source)
 * 
 * Этот класс предоставляет единый интерфейс для работы с кэшем различных типов данных,
 * используемый несколькими фичами приложения. Реализует стратегии кэширования
 * и управление временем жизни данных.
 * 
 * ## Основные обязанности:
 * - Временное хранение общих данных в памяти
 * - Работа с persistent кэшем (Hive/Isar)
 * - Стратегии инвалидации и обновления кэша
 * - Приоритетное кэширование часто используемых данных
 * - Очистка устаревших и неиспользуемых данных
 * 
 * ## Поддерживаемые операции:
 * - Сохранение данных с ключом и TTL (Time To Live)
 * - Получение данных с проверкой валидности
 * - Удаление данных по ключу или паттерну
 * - Очистка всего кэша или по типу данных
 * - Получение статистики использования кэша
 * 
 * ## Используемые библиотеки:
 * - **hive** - для persistent кэширования
 * - **shared_preferences** - для простых key-value пар
 * - **dio** - для кэширования сетевых запросов (опционально)
 * 
 * ## Стратегии кэширования:
 * - **TTL-based** - автоматическое удаление по истечении времени
 * - **LRU (Least Recently Used)** - вытеснение давно не используемых данных
 * - **Size-based** - ограничение по объему памяти
 * - **Priority-based** - приоритетное хранение важных данных
 * 
 * ## Особенности:
 * - Thread-safe операции с кэшем
 * - Поддержка разных типов данных
 * - Автоматическое восстановление при повреждении кэша
 * - Мониторинг hit/miss ratio для оптимизации
 * - Graceful degradation при ошибках кэша
 * 
 * ## Best Practices:
 * - Настраивайте разный TTL для разных типов данных
 * - Используйте асинхронные операции для persistent кэша
 * - Валидируйте данные при чтении из кэша
 * - Реализуйте fallback стратегии при недоступности кэша
 * - Мониторьте использование памяти и производительность
 * - Тестируйте сценарии переполнения кэша
 */

