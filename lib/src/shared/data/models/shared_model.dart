/**
 * Модели данных (Data Models)
 * 
 * Этот файл содержит базовые классы и интерфейсы для Data Transfer Objects (DTO),
 * которые используются для передачи данных между слоями приложения и внешними источниками.
 * 
 * ## Основные обязанности:
 * - Представление структуры данных из API и БД
 * - Сериализация и десериализация JSON
 * - Валидация входящих данных
 * - Преобразование между разными форматами данных
 * - Обеспечение типобезопасности данных
 * 
 * ## Ключевые компоненты:
 * 
 * ### DataTransferObject
 * Базовый интерфейс для всех DTO:
 * - `fromJson` - создание объекта из JSON
 * - `toJson` - преобразование объекта в JSON
 * - `copyWith` - создание копии с изменениями
 * 
 * ### BaseModel
 * Абстрактный класс с общей логикой моделей:
 * - Базовые поля (id, createdAt, updatedAt)
 * - Стандартные методы сравнения и хэширования
 * - Валидация данных модели
 * - Утилиты для работы с датами
 * 
 * ### Специализированные модели:
 * - **ApiModel** - модели для API ответов
 * - **DbModel** - модели для базы данных
 * - **RequestModel** - модели для отправки данных
 * - **ResponseModel** - модели для получения данных
 * 
 * ## Особенности:
 * - Immutable объекты где это возможно
 * - Поддержка разных стратегий сериализации
 * - Валидация данных при создании
 * - Поддержка наследования и композиции
 * - Обработка optional и required полей
 * 
 * ## Best Practices:
 * - Делайте модели immutable для предотвращения side effects
 * - Используйте кодогенерацию для сериализации
 * - Валидируйте данные в конструкторах или factory методах
 * - Документируйте форматы данных и ограничения
 * - Тестируйте сериализацию/десериализацию
 * - Используйте отдельные модели для разных целей
 * - Следите за обратной совместимостью при изменении моделей
 */
