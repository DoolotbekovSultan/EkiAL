  /// –°–æ–∑–¥–∞—Ç—å –ø–æ–ª—É–∂–∏—Ä–Ω—ã–π –≤–∞—Ä–∏–∞–Ω—Ç —Å—Ç–∏–ª—è
  ///
  /// ## –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ:
  /// - –î–ª—è —É–º–µ—Ä–µ–Ω–Ω–æ–≥–æ –∞–∫—Ü–µ–Ω—Ç–∞
  /// - –î–ª—è –ø–æ–¥–∑–∞–≥–æ–ª–æ–≤–∫–æ–≤
  static TextStyle semiBold(TextStyle style) {
    return style.copyWith(fontWeight: FontWeight.w600);
  }

  /// –°–æ–∑–¥–∞—Ç—å —Å—Ç–∏–ª—å —Å –ø—Ä–æ–∑—Ä–∞—á–Ω–æ—Å—Ç—å—é
  ///
  /// ## –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ:
  /// - –î–ª—è disabled —Å–æ—Å—Ç–æ—è–Ω–∏—è
  /// - –î–ª—è –≤—Ç–æ—Ä–æ—Å—Ç–µ–ø–µ–Ω–Ω–æ–≥–æ —Ç–µ–∫—Å—Ç–∞
  /// - –î–ª—è subtle –∞–∫—Ü–µ–Ω—Ç–æ–≤
  static TextStyle withOpacity(TextStyle style, double opacity) {
    return style.copyWith(color: style.color?.withOpacity(opacity));
  }

  /// –°–æ–∑–¥–∞—Ç—å —Å—Ç–∏–ª—å —Å –∫–∞—Å—Ç–æ–º–Ω—ã–º line height
  ///
  /// ## –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ:
  /// - –î–ª—è –ø–ª–æ—Ç–Ω–æ–≥–æ —Ä–∞—Å–ø–æ–ª–æ–∂–µ–Ω–∏—è —Ç–µ–∫—Å—Ç–∞
  /// - –î–ª—è —É–≤–µ–ª–∏—á–µ–Ω–∏—è —á–∏—Ç–∞–±–µ–ª—å–Ω–æ—Å—Ç–∏
  static TextStyle withLineHeight(TextStyle style, double lineHeight) {
